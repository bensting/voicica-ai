/**
 * 获取语言对应的示例文本
 * @param locale 语言区域代码
 * @returns 示例文本，如果不支持则返回 null
 */
export function getSampleText(locale: string): string | null {
  // 精确匹配
  if (SAMPLE_TEXTS[locale]) {
    return SAMPLE_TEXTS[locale];
  }

  // 语系匹配：zh-XX 用 zh-CN，en-XX 用 en-US 等
  const lang = locale.split('-')[0];
  const fallbackLocale = LANG_FALLBACKS[lang];

  if (fallbackLocale && SAMPLE_TEXTS[fallbackLocale]) {
    return SAMPLE_TEXTS[fallbackLocale];
  }

  // 没有配置的语言返回 null，跳过生成
  return null;
}

/**
 * 获取所有支持的语言区域列表
 */
export function getSupportedLocales(): string[] {
  return Object.keys(SAMPLE_TEXTS).filter(key => key !== 'default');
}

/**
 * 检查语言是否支持示例文本
 */
export function isLocaleSupported(locale: string): boolean {
  return getSampleText(locale) !== null;
}